            <p>Most of the time, delays in a space shuttle's launch stem from its complexity and relative fragility. NASA will delay, or scrub, a launch after detecting an existing or potential problem. Delaying the launch can allow threats to dissipate or give officials time to diagnose and repair problems.</p><br><br>
            <p class="p_title">Weather Issues</p>
            <p>Weather is a common cause for delay. Before la­unch, NASA uses weather balloons, Doppler radars, weather radars and other equipment to keep an eye on conditions around the launch pad. Weather conditions that can lead to delay include:</p><br>
            <p>Precipitation: A shuttle cannot take off while any form of precipitation is falling.<br><br>
            Clouds: A shuttle can trigger dangerous lightning when passing through or near cumulus clouds. If clouds are present, officials evaluate and analyze them to determine whether they threaten the safety of the vehicle or its crew.<br><br>
            Wind: A launch cannot take place in winds from the northeast of more than 19 knots or winds from other directions of more than 34 knots.<br><br>
            Ceiling: Members of the U.S. Air Force 45th Space Wing must be able to keep visual contact with the shuttle during liftoff. A ceiling of less than 6,000 feet can make this impossible.<br><br>
            Temperature: Hot temperatures are not usually a problem for shuttle launches. However, temperatures below 48 degrees Fahrenheit (8.88 degrees Celsius) can lead to hazardous ice buildup.</p><br><br>
            <p class="p_title">Mechanical Issue</p>
            <p>Mechanical difficulties are the other primary cause of launch delays. Before launch, people and sensors monitor the shuttle's vital functions. If anyone notices an anomaly, the countdown stops so that workers can find and fix the problem. Mechanical failure in the shuttle's payload can also delay a launch. For example, if the shuttle's mission involves deploying a satellite and the satellite's sensors fail, the launch may be delayed.<br><br>
